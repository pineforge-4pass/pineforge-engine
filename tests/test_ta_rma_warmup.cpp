// --------------------------------------------------------------------
// Tests
// --------------------------------------------------------------------

static void test_rma14_warmup_and_smoothing() {
    ta::RMA rma(14);
    for (int i = 0; i < 13; ++i) {
        double v = rma.compute(kCloses[i]);
        CHECK_NA(v, "RMA(14) warmup bar should be NA");
    }
    // Bar 13 is the seed.
    int exp_idx = 0;
    for (int i = 13; i < (int)kCloses.size(); ++i, ++exp_idx) {
        double got = rma.compute(kCloses[i]);
        char tag[64];
        std::snprintf(tag, sizeof(tag), "RMA(14) bar=%d", i);
        CHECK_EXACT(got, kRMA14_expected[exp_idx], tag);
    }
}

static void test_rsi14_warmup_and_smoothing() {
    ta::RSI rsi(14);
    // Bars 0..13 are warmup (bar 0 has no change; bars 1..13 accumulate
    // into RMA but don't reach the seed count).
    for (int i = 0; i < 14; ++i) {
        double v = rsi.compute(kCloses[i]);
        CHECK_NA(v, "RSI(14) warmup bar should be NA");
    }
    // Bar 14 is the first non-NA.
    int exp_idx = 0;
    for (int i = 14; i < (int)kCloses.size(); ++i, ++exp_idx) {
        double got = rsi.compute(kCloses[i]);
        char tag[64];
        std::snprintf(tag, sizeof(tag), "RSI(14) bar=%d", i);
        CHECK_EXACT(got, kRSI14_expected[exp_idx], tag);
    }
}

// All-up sequence: every bar has positive change, no negative changes.
// rma_dn stays at 0 from the seed onwards => RSI = 100 (short-circuit).
static void test_rsi14_all_up_collapses_to_100() {
    ta::RSI rsi(14);
    for (int i = 0; i < 20; ++i) {
        double close = 10.0 + (double)i;
        double v = rsi.compute(close);
        if (i < 14) {
            char tag[64];
            std::snprintf(tag, sizeof(tag), "RSI(14) all-up warmup bar=%d", i);
            CHECK_NA(v, tag);
        } else {
            char tag[64];
            std::snprintf(tag, sizeof(tag), "RSI(14) all-up bar=%d should be 100", i);
            CHECK_EXACT(v, 100.0, tag);
        }
    }
}

// All-down sequence: every bar has negative change. rma_up stays at 0
// from the seed onwards => rs = 0 / rma_dn = 0 => RSI = 100 - 100/(1+0)
// = 0. Exercises the non-zero-divisor path of the RSI formula.
static void test_rsi14_all_down_collapses_to_0() {
    ta::RSI rsi(14);
    for (int i = 0; i < 20; ++i) {
        double close = 30.0 - (double)i;
        double v = rsi.compute(close);
        if (i < 14) {
            char tag[64];
            std::snprintf(tag, sizeof(tag), "RSI(14) all-down warmup bar=%d", i);
            CHECK_NA(v, tag);
        } else {
            char tag[64];
            std::snprintf(tag, sizeof(tag), "RSI(14) all-down bar=%d should be 0", i);
            CHECK_EXACT(v, 0.0, tag);
        }
    }
}

int main() {
    test_rma14_warmup_and_smoothing();
    test_rsi14_warmup_and_smoothing();
    test_rsi14_all_up_collapses_to_100();
    test_rsi14_all_down_collapses_to_0();

    std::printf("test_ta_rma_warmup: passed=%d failed=%d\n",
                tests_passed, tests_failed);
    return tests_failed == 0 ? 0 : 1;
}
