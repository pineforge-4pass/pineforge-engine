// test_ta_rma_warmup — fixed-fixture correctness for RMA + RSI warmup.
//
// Anchors the RMA / RSI implementation to the *exact* Pine reference
// formula:
//
//   rma := na(rma[1]) ? sma(src, length) : (src + (length - 1) * rma[1]) / length
//
// Two implementations are mathematically equivalent over real numbers:
//
//   (A) (src + (length - 1) * rma[1]) / length         <-- Pine ref
//   (B) alpha * src + (1 - alpha) * rma[1], alpha=1/L  <-- algebraic rewrite
//
// In IEEE-754 double-precision arithmetic they differ by 1-3 ULP per bar
// because the rounding-mode-sensitive operation order is different. Form
// (A) is what TradingView's runtime evaluates, so we use (A) in the
// engine and lock it down here. Reference values were generated by an
// independent Python implementation following the same expression order
// and confirmed by hand-computation on the seed bar.
//
// Coverage:
//   * RMA(14) seed bar value (SMA of first 14 inputs / 14).
//   * RMA(14) first smoothing iteration (bar 14).
//   * RMA(14) deep into smoothing (bar 29).
//   * RSI(14) seed bar (Pine spec: first non-NA at bar `length` = 14
//     using close-to-close changes from bars 1..14, with the first valid
//     change at bar 1).
//   * RSI(14) several bars deep into smoothing.
//   * RSI(14) limit cases:
//        - all-up sequence collapses to 100 (rma_down == 0 short-circuit).
//        - all-down sequence collapses to 0 (rma_up == 0).
//
// The fixture is small (30 bars) and fully reproducible: the close
// series, expected RMA values, and expected RSI values are all hard-
// coded below. If you intentionally change the RMA / RSI implementation
// in src/ta_moving_averages.cpp or src/ta_oscillators.cpp this test will
// catch the per-bar ULP drift immediately.

#include <pineforge/ta.hpp>
#include <pineforge/na.hpp>

#include <cmath>
#include <cstdio>
#include <vector>

using namespace pineforge;

static int tests_passed = 0;
static int tests_failed = 0;

// Bit-exact equality is the right check for IEEE-754 reference values:
// either we match the Pine formula to the last bit, or we don't. ULP
// tolerance would silently re-introduce the algebraic-rewrite drift.
static bool exact_eq(double a, double b) {
    if (is_na(a) && is_na(b)) return true;
    if (is_na(a) || is_na(b)) return false;
    return a == b;
}

#define CHECK_EXACT(actual, expected, tag) do { \
    double _a = (actual); \
    double _e = (expected); \
    if (!exact_eq(_a, _e)) { \
        std::printf("FAIL: %s (line %d): got %.17g expected %.17g (diff=%.17g)\n", \
                    tag, __LINE__, _a, _e, _a - _e); \
        ++tests_failed; \
    } else { \
        ++tests_passed; \
    } \
} while (0)

#define CHECK_NA(actual, tag) do { \
    if (!is_na(actual)) { \
        std::printf("FAIL: %s (line %d): got %.17g expected NA\n", tag, __LINE__, (actual)); \
        ++tests_failed; \
    } else { \
        ++tests_passed; \
    } \
} while (0)

